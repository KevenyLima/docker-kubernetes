// estrutura de um comando em linux 
// comando -opções arquivos/diretórios

// ctrl + alt + t --> abre o terminal do linux

// ls -ltr --> lista arquivos e diretórios -ltr(lista detalhada em ordem de modificação)

// ls -a --> lista arquivos ocultos que o ls normalmente nao listaria 

// ls lh --> legível para humano (formata os dados mais bonitinho)

// ls -l {diretório que deseja listar} --> lista os arquivos de outro diretório

// ls -lr --> lista em ordem reversa

// ls -R --> lista os diretórios e os subdiretórios

// ls -lS --> lista pelo tamanho do arquivo

// cat {nome do aquivo} --> mostra o conteúdo de um arquivo

// cat {arquivo 1 } {arquivo 2} > {nome do novo arquivo} --> junta os conteúdos dos dois arquivos em um novo arquivo

// cat -n {nome do arquivo} --> mostra o conteúdo do arquivo em linhas numeradas

// cat {nome do arquivo} >> {nome do arquivo que sera concatenado} --> adiciona o conteúdo do primeiro arquivo no segundo arquivo sem perda de conteúdo (concatenação)

// touch {nome do arquivo}.{extensão do arquivo} --> cria um novo arquivo no diretório atual

// touch {nome do arquivo que ja existe} --> atualiza o timestamp para a data atual

// man {nome do comando} --> lista todo o manual com as possíveis flags para o commando

